package week1.day1;

public class LearnOperators {
	

	int a = 10;
	int b = 20;
	int sum = a+b;
	int sub = a-b;
	int multiply = a*b;
	int div = multiply/a;
	

	public static void main(String[] args) {
		LearnOperators lo = new LearnOperators();
		System.out.println(lo.sum);
		System.out.println(lo.sub);
		System.out.println(lo.multiply);
		System.out.println(lo.div);
		

	}

}
